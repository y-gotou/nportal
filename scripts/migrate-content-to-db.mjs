/**
 * コンテンツファイル（Markdown/JSON）のデータを D1 データベースへ移行するスクリプト。
 *
 * 実行方法:
 *   node scripts/migrate-content-to-db.mjs          # SQLファイルを生成
 *   wrangler d1 execute nportal-db --local --file db/migrate_content.sql  # ローカルに適用
 *   wrangler d1 execute nportal-db --remote --file db/migrate_content.sql # 本番に適用
 */

import fs from "fs";
import path from "path";
import matter from "gray-matter";
import { remark } from "remark";
import gfm from "remark-gfm";
import html from "remark-html";

const projectRoot = process.cwd();
const minutesDir = path.join(projectRoot, "content/minutes");
const scheduleFile = path.join(projectRoot, "content/schedule.json");
const resourcesFile = path.join(projectRoot, "content/resources.json");
const outFile = path.join(projectRoot, "db/migrate_content.sql");

async function renderMarkdown(markdown) {
  const result = await remark().use(gfm).use(html).process(markdown);
  return result.toString();
}

function escapeSql(str) {
  return String(str ?? "").replace(/'/g, "''");
}

async function buildMinutesStatements() {
  if (!fs.existsSync(minutesDir)) return [];

  const files = fs.readdirSync(minutesDir).filter((f) => f.endsWith(".md"));
  const statements = [];

  for (const filename of files) {
    const slug = filename.replace(/\.md$/, "");
    const filePath = path.join(minutesDir, filename);
    const fileContents = fs.readFileSync(filePath, "utf8");
    const { data, content } = matter(fileContents);
    const contentHtml = await renderMarkdown(content);

    const title = escapeSql(data.title ?? slug);
    const date = escapeSql(data.date ?? "");
    const attendees = escapeSql(JSON.stringify(data.attendees ?? []));
    const topics = escapeSql(JSON.stringify(data.topics ?? []));
    const contentMd = escapeSql(content);
    const contentHtmlEsc = escapeSql(contentHtml);

    statements.push(
      `INSERT OR IGNORE INTO minutes (slug, title, date, attendees, topics, content_md, content_html) VALUES ('${escapeSql(slug)}', '${title}', '${date}', '${attendees}', '${topics}', '${contentMd}', '${contentHtmlEsc}');`,
    );
  }

  return statements;
}

function buildScheduleStatements() {
  if (!fs.existsSync(scheduleFile)) return [];

  const items = JSON.parse(fs.readFileSync(scheduleFile, "utf8"));
  return items.map((item) => {
    const meetingUrl = item.meetingUrl ? `'${escapeSql(item.meetingUrl)}'` : "NULL";
    const minutesSlug = item.minutesSlug ? `'${escapeSql(item.minutesSlug)}'` : "NULL";
    const location = item.location ? `'${escapeSql(item.location)}'` : "NULL";
    const topics = escapeSql(JSON.stringify(item.topics ?? []));
    return `INSERT OR IGNORE INTO schedule (id, date, time, title, meeting_url, minutes_slug, topics, presenter, location) VALUES (${item.id}, '${escapeSql(item.date)}', '${escapeSql(item.time)}', '${escapeSql(item.title)}', ${meetingUrl}, ${minutesSlug}, '${topics}', '${escapeSql(item.presenter)}', ${location});`;
  });
}

function buildResourcesStatements() {
  if (!fs.existsSync(resourcesFile)) return [];

  const items = JSON.parse(fs.readFileSync(resourcesFile, "utf8"));
  return items.map((item) => {
    const presenter = item.presenter ? `'${escapeSql(item.presenter)}'` : "NULL";
    const relatedMinutesSlug = item.relatedMinutesSlug
      ? `'${escapeSql(item.relatedMinutesSlug)}'`
      : "NULL";
    const tags = escapeSql(JSON.stringify(item.tags ?? []));
    return `INSERT OR IGNORE INTO resources (id, title, url, type, tags, date, presenter, related_minutes_slug) VALUES (${item.id}, '${escapeSql(item.title)}', '${escapeSql(item.url)}', '${escapeSql(item.type)}', '${tags}', '${escapeSql(item.date)}', ${presenter}, ${relatedMinutesSlug});`;
  });
}

async function main() {
  const lines = [
    "-- Content migration: minutes, schedule, resources",
    "-- Generated by scripts/migrate-content-to-db.mjs",
    "",
  ];

  const minutesStatements = await buildMinutesStatements();
  if (minutesStatements.length > 0) {
    lines.push("-- minutes");
    lines.push(...minutesStatements);
    lines.push("");
  }

  const scheduleStatements = buildScheduleStatements();
  if (scheduleStatements.length > 0) {
    lines.push("-- schedule");
    lines.push(...scheduleStatements);
    lines.push("");
  }

  const resourcesStatements = buildResourcesStatements();
  if (resourcesStatements.length > 0) {
    lines.push("-- resources");
    lines.push(...resourcesStatements);
    lines.push("");
  }

  fs.writeFileSync(outFile, lines.join("\n"));
  console.log(`Generated: ${outFile}`);
  console.log(`  Minutes:   ${minutesStatements.length} entries`);
  console.log(`  Schedule:  ${scheduleStatements.length} entries`);
  console.log(`  Resources: ${resourcesStatements.length} entries`);
  console.log("\nNext steps:");
  console.log("  Local:   wrangler d1 execute nportal-db --local --file db/migrate_content.sql");
  console.log("  Prod:    wrangler d1 execute nportal-db --remote --file db/migrate_content.sql");
  console.log("  Preview: wrangler d1 execute nportal-preview-db --remote --file db/migrate_content.sql");
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
